package week4_2_page_52;

public class Rectangle {
    int l,b;
    Rectangle()
    {
        this.l = 0;
        this.b = 0;
    }
    Rectangle(int l)
    {
        this.l = l;
        this.b= b;
    }
    Rectangle(int l, int b)
    {
        this.l = l;
        this.b = b;
    }

    public void areaOfRectangle()
    {
        System.out.println(+l*b);
    }

}
